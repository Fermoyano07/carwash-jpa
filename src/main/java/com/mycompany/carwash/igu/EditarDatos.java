    private void cargarDatos(int id_vehiculo) {
         this.vehiculo = control.traerVehiculo(id_vehiculo);
        
        txtMarca.setText(vehiculo.getMarca());
        txtModelo.setText(vehiculo.getModelo());
        txtColor.setText(vehiculo.getColor());
        txtNombreDuenio.setText(vehiculo.getUnDuenio().getNombre_Duenio());
        txtTelefono.setText(vehiculo.getUnDuenio().getTelDuenio());
        txtObservaciones.setText(vehiculo.getObservaciones());
        
        if (vehiculo.getTipo().equals("Automóvil")) {
            cmbVehiculo.setSelectedIndex(1);
        }
        else {
            if (vehiculo.getTipo().equals("Camioneta")) {
                cmbVehiculo.setSelectedIndex(2);
            }
            else {
                if (vehiculo.getTipo().equals("Motocicleta")) {
                    cmbVehiculo.setSelectedIndex(3);
                }
            }
        }
    }
    
