    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        //Datos del vehiculo
        String marcaVehiculo = txtMarca.getText();
        String modeloVehiculo = txtModelo.getText();
        String colorVehiculo = txtColor.getText();
        String observaciones = txtObservaciones.getText();
        String tipoVehiculo = (String) cmbVehiculo.getSelectedItem();
        
        //Datos del dueño
        String nombreDuenio = txtNombreDuenio.getText();
        String telDuenio = txtTelefono.getText();
        
        control.editarVehiculo(vehiculo, marcaVehiculo, modeloVehiculo, colorVehiculo, observaciones, 
                tipoVehiculo, nombreDuenio, telDuenio);
        
        //Mensaje de confirmacion
        mostrarMensaje("Edición realizada correctamente", "Info", "¡Edición correcta!");
        
        //Llamar a la ventana de VerDatos
        VerDatos pantalla = new VerDatos();
        pantalla.setVisible(true);
        pantalla.setLocationRelativeTo(null);
        
        //Cerrar Ventana
        this.dispose();
        
    }//GEN-LAST:event_btnGuardarActionPerformed

